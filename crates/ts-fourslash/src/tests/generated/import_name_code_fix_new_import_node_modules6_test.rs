#[test]
pub fn test_import_name_code_fix_new_import_node_modules6() {
    let mut t = TestingT;
    run_test_import_name_code_fix_new_import_node_modules6(&mut t);
}

fn run_test_import_name_code_fix_new_import_node_modules6(t: &mut TestingT) {
    if should_skip_if_failing("TestImportNameCodeFixNewImportNodeModules6") {
        return;
    }
    let content = r#"[|f1/*0*/('');|]
// @Filename: package.json
{ "dependencies": { "package-name": "latest" } }
// @Filename: node_modules/package-name/bin/lib/index.d.ts
export function f1(text: string): string;
// @Filename: node_modules/package-name/bin/lib/index.js
function f1(text) { }
exports.f1 = f1;
// @Filename: node_modules/package-name/package.json
{
  "main": "bin/lib/index.js",
  "types": "bin/lib/index.d.ts"
}"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.verify_import_fix_at_position(
        t,
        &vec![
            r#"import { f1 } from "package-name";

f1('');"#
                .to_string(),
        ],
        None,
    );
    done();
}
