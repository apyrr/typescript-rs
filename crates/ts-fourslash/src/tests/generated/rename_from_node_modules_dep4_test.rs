#[test]
pub fn test_rename_from_node_modules_dep4() {
    let mut t = TestingT;
    run_test_rename_from_node_modules_dep4(&mut t);
}

fn run_test_rename_from_node_modules_dep4(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r#"// @Filename: /index.ts
import hljs from "highlight.js/lib/core"
import { h } from "highlight.js/lib/core";
import { /*notOk*/h as hh } from "highlight.js/lib/core";
/*ok*/[|hljs|];
/*okWithAlias*/[|h|];
/*ok2*/[|hh|];
// @Filename: /node_modules/highlight.js/lib/core.d.ts
declare const hljs: { registerLanguage(s: string): void };
export default hljs;
export const h: string;
// @Filename: /tsconfig.json
{}"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.go_to_marker(t, "ok");
    f.verify_rename_succeeded_at_current_position();
    f.verify_rename_succeeded_at_current_position();
    f.go_to_marker(t, "ok2");
    f.verify_rename_succeeded_at_current_position();
    f.verify_rename_succeeded_at_current_position();
    f.go_to_marker(t, "notOk");
    f.verify_rename_failed_at_current_position();
    f.verify_rename_failed_at_current_position();
    f.go_to_marker(t, "okWithAlias");
    f.verify_rename_succeeded_at_current_position();
    f.verify_rename_failed_at_current_position();
    done();
}
