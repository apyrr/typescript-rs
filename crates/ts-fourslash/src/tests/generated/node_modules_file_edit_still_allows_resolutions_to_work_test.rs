#[test]
pub fn test_node_modules_file_edit_still_allows_resolutions_to_work() {
    let mut t = TestingT;
    run_test_node_modules_file_edit_still_allows_resolutions_to_work(&mut t);
}

fn run_test_node_modules_file_edit_still_allows_resolutions_to_work(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r##"// @Filename: /tsconfig.json
{ "compilerOptions": { "module": "nodenext", "strict": true } }
// @Filename: /package.json
{ "type": "module", "imports": { "#foo": "./foo.cjs" } }
// @Filename: /foo.cts
export const x = 1;
// @Filename: /index.ts
import * as mod from "#foo";
/**/"##;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.go_to_marker(t, "");
    f.insert(t, "mod.x");
    f.verify_no_errors();
    f.verify_suggestion_diagnostics(&[]);
    done();
}
