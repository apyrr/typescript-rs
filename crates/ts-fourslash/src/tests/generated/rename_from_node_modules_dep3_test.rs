#[test]
pub fn test_rename_from_node_modules_dep3() {
    let mut t = TestingT;
    run_test_rename_from_node_modules_dep3(&mut t);
}

fn run_test_rename_from_node_modules_dep3(t: &mut TestingT) {
    if should_skip_if_failing("TestRenameFromNodeModulesDep3") {
        return;
    }
    let content = r#"// @Filename: /packages/first/index.d.ts
import { /*ok*/[|Foo|] } from "foo";
declare type FooBar = Foo[/*ok2*/"[|bar|]"];
// @Filename: /packages/foo/package.json
 { "types": "index.d.ts" }
// @Filename: /packages/foo/index.d.ts
export interface Foo {
    /*ok3*/[|bar|]: string;
}
// @link: /packages/foo -> /packages/first/node_modules/foo"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.go_to_marker(t, "ok");
    f.verify_rename_succeeded_at_current_position();
    f.verify_rename_succeeded_at_current_position();
    f.go_to_marker(t, "ok2");
    f.verify_rename_succeeded_at_current_position();
    f.go_to_marker(t, "ok3");
    f.verify_rename_succeeded_at_current_position();
    done();
}
