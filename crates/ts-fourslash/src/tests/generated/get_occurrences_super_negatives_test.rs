#[test]
pub fn test_get_occurrences_super_negatives() {
    let mut t = TestingT;
    run_test_get_occurrences_super_negatives(&mut t);
}

fn run_test_get_occurrences_super_negatives(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r"function f(x = [|super|]) {
    [|super|];
}

namespace M {
    [|super|];
    function f(x = [|super|]) {
    [|super|];
    }

    class A {
    }

    class B extends A {
        constructor() {
            super();
        }
    }
}";
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.verify_baseline_document_highlights(
        t,
        None,
        f.ranges()
            .into_iter()
            .map(MarkerOrRangeOrName::Range)
            .collect(),
    );
    done();
}
