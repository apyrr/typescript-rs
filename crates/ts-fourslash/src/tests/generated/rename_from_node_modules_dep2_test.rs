#[test]
pub fn test_rename_from_node_modules_dep2() {
    let mut t = TestingT;
    run_test_rename_from_node_modules_dep2(&mut t);
}

fn run_test_rename_from_node_modules_dep2(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r#"// @Filename: /node_modules/first/index.d.ts
import { /*okWithAlias*/[|Foo|] } from "foo";
declare type FooBar = Foo[/*notOk*/"bar"];
// @Filename: /node_modules/first/node_modules/foo/package.json
 { "types": "index.d.ts" }
// @Filename: /node_modules/first/node_modules/foo/index.d.ts
export interface Foo {
    /*ok2*/[|bar|]: string;
}
// @Filename: /node_modules/first/node_modules/foo/bar.d.ts
import { Foo } from "./index";
declare type FooBar = Foo[/*ok3*/"[|bar|]"];"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.go_to_marker(t, "okWithAlias");
    f.verify_rename_succeeded_at_current_position();
    f.verify_rename_failed_at_current_position();
    f.go_to_marker(t, "notOk");
    f.verify_rename_failed_at_current_position();
    f.go_to_marker(t, "ok2");
    f.verify_rename_succeeded_at_current_position();
    f.go_to_marker(t, "ok3");
    f.verify_rename_succeeded_at_current_position();
    done();
}
