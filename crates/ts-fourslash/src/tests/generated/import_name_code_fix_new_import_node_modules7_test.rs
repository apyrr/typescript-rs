#[test]
pub fn test_import_name_code_fix_new_import_node_modules7() {
    let mut t = TestingT;
    run_test_import_name_code_fix_new_import_node_modules7(&mut t);
}

fn run_test_import_name_code_fix_new_import_node_modules7(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r#"[|f1/*0*/('');|]
// @Filename: package.json
{ "dependencies": { "package-name": "0.0.1" } }
// @Filename: node_modules/package-name/bin/lib/libfile.d.ts
export declare function f1(text: string): string;
// @Filename: node_modules/package-name/bin/lib/libfile.js
function f1(text) {}
exports.f1 = f1;
// @Filename: node_modules/package-name/package.json
{ "main": "bin/lib/libfile.js" }"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.verify_import_fix_at_position(
        t,
        &vec![
            r#"import { f1 } from "package-name";

f1('');"#
                .to_string(),
        ],
        None,
    );
    done();
}
