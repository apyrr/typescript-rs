#[test]
pub fn test_auto_import_node12_node_modules1() {
    let mut t = TestingT;
    run_test_auto_import_node12_node_modules1(&mut t);
}

fn run_test_auto_import_node12_node_modules1(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r"// @lib: es5
// @module: node16
// @Filename: /node_modules/undici/index.d.ts
export function request(): any;
// @Filename: /index.mts
request/**/";
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.verify_import_fix_module_specifiers(t, "", &vec!["undici".to_string()], None);
    done();
}
