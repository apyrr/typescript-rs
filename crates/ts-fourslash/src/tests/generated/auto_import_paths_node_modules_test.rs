use crate::generated_prelude::*;
use ts_core as core;
use ts_ls as lsutil;
use ts_lsproto as lsproto;
use ts_modulespecifiers as modulespecifiers;

#[test]
pub fn test_auto_import_paths_node_modules() {
    let mut t = TestingT;
    run_test_auto_import_paths_node_modules(&mut t);
}

fn run_test_auto_import_paths_node_modules(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r#"// @Filename: tsconfig.json
{
    "compilerOptions": {
        "module": "amd",
        "moduleResolution": "node",
        "rootDir": "ts",
        "baseUrl": ".",
        "paths": {
            "*": ["node_modules/@woltlab/wcf/ts/*"]
        }
    },
    "include": [
        "ts",
        "node_modules/@woltlab/wcf/ts",
     ]
}
// @Filename: node_modules/@woltlab/wcf/ts/WoltLabSuite/Core/Component/Dialog.ts
export class Dialog {}
// @Filename: ts/main.ts
Dialog/**/"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.verify_import_fix_module_specifiers(
        t,
        "",
        &vec!["WoltLabSuite/Core/Component/Dialog".to_string()],
        None,
    );
    done();
}
