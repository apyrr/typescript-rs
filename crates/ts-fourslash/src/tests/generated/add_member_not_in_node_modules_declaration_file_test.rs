#[test]
pub fn test_add_member_not_in_node_modules_declaration_file() {
    let mut t = TestingT;
    run_test_add_member_not_in_node_modules_declaration_file(&mut t);
}

fn run_test_add_member_not_in_node_modules_declaration_file(t: &mut TestingT) {
    if should_skip_if_failing("TestAddMemberNotInNodeModulesDeclarationFile") {
        return;
    }
    let content = r#"// @noImplicitReferences: true
// @traceResolution: true
// @Filename: /node_modules/foo/types.d.ts
interface Response {}
// @Filename: /node_modules/foo/package.json
{ "types": "types.d.ts" }
// @Filename: /foo.ts
import { Response } from 'foo'
declare const resp: Response
resp.test()"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.go_to_file(t, "/foo.ts");
    f.verify_code_fix_not_available(t, &[]);
    done();
}
