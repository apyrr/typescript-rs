#[test]
pub fn test_import_name_code_fix_new_import_node_modules2() {
    let mut t = TestingT;
    run_test_import_name_code_fix_new_import_node_modules2(&mut t);
}

fn run_test_import_name_code_fix_new_import_node_modules2(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r#"[|f1/*0*/();|]
// @Filename: ../package.json
{ "dependencies": { "fake-module": "latest" } }
// @Filename: ../node_modules/fake-module/notindex.d.ts
export var v1 = 5;
export function f1();
// @Filename: ../node_modules/fake-module/notindex.js
module.exports = {
   v1: 5,
   f1: function () {}
};
// @Filename: ../node_modules/fake-module/package.json
{ "main":"./notindex.js", "typings":"./notindex.d.ts" }"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.verify_import_fix_at_position(
        t,
        &vec![
            r#"import { f1 } from "fake-module";

f1();"#
                .to_string(),
        ],
        None,
    );
    done();
}
