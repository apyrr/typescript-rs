#![allow(non_snake_case)]
#![allow(unused_imports)]

use crate::generated_prelude::*;
use ts_core as core;
use ts_ls as lsutil;
use ts_lsproto as lsproto;
use ts_modulespecifiers as modulespecifiers;

#[test]
pub fn test_import_name_code_fix_avoid_relative_node_modules() {
    let mut t = TestingT;
    run_test_import_name_code_fix_avoid_relative_node_modules(&mut t);
}

fn run_test_import_name_code_fix_avoid_relative_node_modules(t: &mut TestingT) {
    if should_skip_if_failing("TestImportNameCodeFix_avoidRelativeNodeModules") {
        return;
    }
    let content = r#"// @Filename: /a/index.d.ts
// @Symlink: /b/node_modules/a/index.d.ts
// @Symlink: /c/node_modules/a/index.d.ts
export const a: number;
// @Filename: /b/index.ts
// @Symlink: /c/node_modules/b/index.d.ts
import { a } from 'a'
export const b: number;
// @Filename: /c/a_user.ts
import { a } from "a";
// @Filename: /c/foo.ts
[|import { b } from "b";
a;|]"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.go_to_file(t, "/c/foo.ts");
    f.verify_import_fix_at_position(
        t,
        &vec![r#"import { a } from "a";
import { b } from "b";
a;"#
        .to_string()],
        None,
    );
    done();
}
