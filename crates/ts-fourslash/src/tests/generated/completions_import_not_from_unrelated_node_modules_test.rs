#![allow(non_snake_case)]
#![allow(unused_imports)]

use crate::generated_prelude::*;
use ts_core as core;
use ts_ls as lsutil;
use ts_lsproto as lsproto;
use ts_modulespecifiers as modulespecifiers;

#[test]
pub fn test_completions_import_not_from_unrelated_node_modules() {
    let mut t = TestingT;
    run_test_completions_import_not_from_unrelated_node_modules(&mut t);
}

fn run_test_completions_import_not_from_unrelated_node_modules(t: &mut TestingT) {
    if should_skip_if_failing("TestCompletionsImport_notFromUnrelatedNodeModules") {
        return;
    }
    let content = r"// @module: esnext
// @Filename: /unrelated/node_modules/@types/foo/index.d.ts
export function foo() {}
// @Filename: /src/b.ts
fo/**/;";
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.verify_completions(
        t,
        MarkerInput::Name("".to_string()),
        Some(&CompletionsExpectedList {
            is_incomplete: false,
            item_defaults: Some(CompletionsExpectedItemDefaults {
                commit_characters: Some(default_commit_characters()),
                edit_range: ExpectedCompletionEditRange::Ignored,
            }),
            items: Some(CompletionsExpectedItems {
                includes: Vec::new(),
                excludes: vec!["foo".to_string()],
                exact: Vec::new(),
                unsorted: Vec::new(),
            }),
            user_preferences: None,
        }),
    );
    done();
}
