#[test]
pub fn test_get_edits_for_file_rename_directory_no_update_node_modules_import() {
    let mut t = TestingT;
    run_test_get_edits_for_file_rename_directory_no_update_node_modules_import(&mut t);
}

fn run_test_get_edits_for_file_rename_directory_no_update_node_modules_import(t: &mut TestingT) {
    if should_skip_if_failing("TestGetEditsForFileRename_directory_noUpdateNodeModulesImport") {
        return;
    }
    let content = r#"// @Filename: /a/b/file1.ts
import { foo } from "foo";
// @Filename: /a/b/node_modules/foo/index.d.ts
export const foo = 0;"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.verify_will_rename_files_edits(
        t,
        "/a/b",
        "/a/d",
        std::collections::HashMap::<String, String>::new(),
    );
    done();
}
