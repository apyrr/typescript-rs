#[test]
pub fn test_member_list_in_function_call() {
    let mut t = TestingT;
    run_test_member_list_in_function_call(&mut t);
}

fn run_test_member_list_in_function_call(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r#"function aa(x: any) {}
aa({
  "1": function () {
    var b = "";
    b/**/;
  }
});"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.go_to_marker(t, "");
    f.insert(t, ".");
    f.verify_completions(
        t,
        MarkerInput::None,
        Some(&CompletionsExpectedList {
            is_incomplete: false,
            item_defaults: Some(CompletionsExpectedItemDefaults {
                commit_characters: Some(default_commit_characters()),
                edit_range: ExpectedCompletionEditRange::Ignored,
            }),
            items: Some(CompletionsExpectedItems {
                includes: vec![CompletionsExpectedItem::Label("charAt".to_string())],
                excludes: Vec::new(),
                exact: Vec::new(),
                unsorted: Vec::new(),
            }),
            user_preferences: None,
        }),
    );
    done();
}
