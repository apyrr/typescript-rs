#[test]
pub fn test_auto_import_bundler_block_relative_node_modules_paths() {
    let mut t = TestingT;
    run_test_auto_import_bundler_block_relative_node_modules_paths(&mut t);
}

fn run_test_auto_import_bundler_block_relative_node_modules_paths(t: &mut TestingT) {
    skip_if_failing(t);
    let content = r#"// @module: esnext
// @moduleResolution: bundler
// @Filename: /node_modules/dep/package.json
{
  "name": "dep",
  "version": "1.0.0",
  "exports": "./dist/index.js"
}
// @Filename: /node_modules/dep/dist/utils.d.ts
export const util: () => void;
// @Filename: /node_modules/dep/dist/index.d.ts
export * from "./utils";
// @Filename: /index.ts
util/**/"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.verify_import_fix_module_specifiers(t, "", &vec!["dep".to_string()], None);
    done();
}
