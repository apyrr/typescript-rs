#[test]
pub fn test_rename_from_node_modules_dep1() {
    let mut t = TestingT;
    run_test_rename_from_node_modules_dep1(&mut t);
}

fn run_test_rename_from_node_modules_dep1(t: &mut TestingT) {
    if should_skip_if_failing("TestRenameFromNodeModulesDep1") {
        return;
    }
    let content = r#"// @Filename: /index.ts
import { /*okWithAlias*/[|Foo|] } from "foo";
declare const f: Foo;
f./*notOk*/bar;
// @Filename: /tsconfig.json
 { }
// @Filename: /node_modules/foo/package.json
 { "types": "index.d.ts" }
// @Filename: /node_modules/foo/index.d.ts
export interface Foo {
    bar: string;
}"#;
    let (mut f, done) = new_fourslash(t, None /*capabilities*/, content.to_string());
    f.go_to_marker(t, "okWithAlias");
    f.verify_rename_succeeded_at_current_position();
    f.verify_rename_failed_at_current_position();
    f.go_to_marker(t, "notOk");
    f.verify_rename_failed_at_current_position();
    done();
}
