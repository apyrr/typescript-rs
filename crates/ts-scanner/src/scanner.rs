pub struct SkipTriviaOptions {
    pub stop_after_line_break: bool,
    pub stop_at_comments: bool,
}

pub fn skip_trivia(text: &str, pos: usize) -> usize {
    skip_trivia_ex(text, pos, None)
}

pub fn skip_trivia_ex(text: &str, mut pos: usize, options: Option<&SkipTriviaOptions>) -> usize {
    while pos < text.len() {
        if is_shebang_trivia(text, pos) {
            pos = scan_shebang_trivia(text, pos);
            continue;
        }
        if is_conflict_marker_trivia(text, pos) {
            pos = scan_conflict_marker_trivia(text, pos);
            continue;
        }
        let rest = &text[pos..];
        if rest.starts_with("//") {
            if options.is_some_and(|options| options.stop_at_comments) {
                return pos;
            }
            pos = scan_line_comment(text, pos);
            continue;
        }
        if rest.starts_with("/*") {
            if options.is_some_and(|options| options.stop_at_comments) {
                return pos;
            }
            pos = scan_block_comment(text, pos);
            continue;
        }
        let Some(ch) = rest.chars().next() else {
            return pos;
        };
        if ch == '\r' || ch == '\n' {
            pos += ch.len_utf8();
            if ch == '\r' && text[pos..].starts_with('\n') {
                pos += 1;
            }
            if options.is_some_and(|options| options.stop_after_line_break) {
                return pos;
            }
            continue;
        }
        if is_white_space_like(ch) {
            pos += ch.len_utf8();
            continue;
        }
        return pos;
    }
    pos
}

pub fn is_conflict_marker_trivia(text: &str, pos: usize) -> bool {
    // Conflict markers must be at the start of a line.
    if pos > 0 && !matches!(text.as_bytes().get(pos - 1), Some(b'\n' | b'\r')) {
        return false;
    }
    let Some(&ch) = text.as_bytes().get(pos) else {
        return false;
    };
    if !matches!(ch, b'<' | b'|' | b'=' | b'>') {
        return false;
    }
    if pos + MERGE_CONFLICT_MARKER_LENGTH >= text.len() {
        return false;
    }
    for i in 0..MERGE_CONFLICT_MARKER_LENGTH {
        if text.as_bytes()[pos + i] != ch {
            return false;
        }
    }
    ch == b'=' || text.as_bytes()[pos + MERGE_CONFLICT_MARKER_LENGTH] == b' '
}

pub fn scan_conflict_marker_trivia(text: &str, pos: usize) -> usize {
    let bytes = text.as_bytes();
    let Some(&ch) = bytes.get(pos) else {
        return pos;
    };
    let len = text.len();

    if ch == b'<' || ch == b'>' {
        scan_line_comment(text, pos)
    } else {
        debug_assert!(ch == b'|' || ch == b'=');
        // Consume everything from the start of a ||||||| or ======= marker to the start
        // of the next ======= or >>>>>>> marker.
        let mut pos = pos;
        while pos < len {
            let current_char = bytes[pos];
            if (current_char == b'=' || current_char == b'>')
                && current_char != ch
                && is_conflict_marker_trivia(text, pos)
            {
                break;
            }

            pos += 1;
        }
        pos
    }
}

pub fn is_shebang_trivia(text: &str, pos: usize) -> bool {
    pos == 0 && text.starts_with("#!")
}

pub fn scan_shebang_trivia(text: &str, pos: usize) -> usize {
    scan_line_comment(text, pos)
}

pub fn get_shebang(text: &str) -> String {
    if is_shebang_trivia(text, 0) {
        return text[..scan_shebang_trivia(text, 0)].to_string();
    }
    String::new()
}

pub fn get_scanner_for_source_file(
    source_file: &impl ast::SourceFileStoreLike,
    pos: usize,
) -> Scanner {
    let mut scanner = Scanner::new(
        source_file.data().shared_text(),
        core::SCRIPT_TARGET_LATEST_STANDARD,
    );
    scanner.language_variant = source_file.data().language_variant();
    scanner.pos = pos;
    scanner.scan();
    scanner
}

pub fn scan_token_at_position(
    source_file: &impl ast::SourceFileStoreLike,
    pos: usize,
) -> ast::Kind {
    get_scanner_for_source_file(source_file, pos).token
}

pub fn get_range_of_token_at_position(
    source_file: &impl ast::SourceFileStoreLike,
    pos: usize,
) -> core::TextRange {
    let scanner = get_scanner_for_source_file(source_file, pos);
    core::new_text_range(scanner.token_start as i32, scanner.pos as i32)
}

fn node_pos(source_file: &impl ast::SourceFileStoreLike, node: ast::Node) -> i32 {
    source_file.store().loc(node).pos()
}

fn node_end(source_file: &impl ast::SourceFileStoreLike, node: ast::Node) -> i32 {
    source_file.store().loc(node).end()
}

fn node_kind(source_file: &impl ast::SourceFileStoreLike, node: ast::Node) -> ast::Kind {
    source_file.store().kind(node)
}

fn node_is_missing(source_file: &impl ast::SourceFileStoreLike, node: ast::Node) -> bool {
    node_pos(source_file, node) == node_end(source_file, node)
}

pub fn get_token_pos_of_node(
    node: &ast::Node,
    source_file: &impl ast::SourceFileStoreLike,
    _include_comments: bool,
) -> usize {
    // With missing nodes, Go intentionally does not skip trivia because doing
    // so would jump to the next real token.
    if node_is_missing(source_file, *node) {
        return node_pos(source_file, *node).max(0) as usize;
    }
    if node_kind(source_file, *node) == ast::Kind::JsxText {
        return skip_trivia_ex(
            source_file.data().text(),
            node_pos(source_file, *node).max(0) as usize,
            Some(&SkipTriviaOptions {
                stop_after_line_break: false,
                stop_at_comments: true,
            }),
        );
    }
    skip_trivia_ex(
        source_file.data().text(),
        node_pos(source_file, *node).max(0) as usize,
        Some(&SkipTriviaOptions {
            stop_after_line_break: false,
            stop_at_comments: false,
        }),
    )
}

fn get_error_range_for_arrow_function(
    source_file: &impl ast::SourceFileStoreLike,
    node: &ast::Node,
) -> core::TextRange {
    let pos = skip_trivia(
        source_file.data().text(),
        node_pos(source_file, *node).max(0) as usize,
    );
    if let Some(body) = source_file.store().body(*node)
        && node_kind(source_file, body) == ast::Kind::Block
    {
        let start_line = get_ecma_line_of_position(
            source_file.data(),
            node_pos(source_file, body).max(0) as usize,
        );
        let end_line = get_ecma_line_of_position(
            source_file.data(),
            node_end(source_file, body).max(0) as usize,
        );
        if start_line < end_line {
            return core::new_text_range(
                pos as i32,
                get_ecma_end_line_position(source_file.data(), start_line) as i32 + 1,
            );
        }
    }
    core::new_text_range(pos as i32, node_end(source_file, *node))
}

pub fn get_error_range_for_node(
    source_file: &impl ast::SourceFileStoreLike,
    node: &ast::Node,
) -> core::TextRange {
    enum ErrorNode<'a> {
        Borrowed(&'a ast::Node),
        Owned(ast::Node),
        Missing,
    }

    impl<'a> ErrorNode<'a> {
        fn as_ref(&'a self) -> Option<&'a ast::Node> {
            match self {
                ErrorNode::Borrowed(node) => Some(node),
                ErrorNode::Owned(node) => Some(node),
                ErrorNode::Missing => None,
            }
        }
    }

    let error_node = match node_kind(source_file, *node) {
        ast::Kind::SourceFile => {
            let pos = skip_trivia(source_file.data().text(), 0);
            if pos == source_file.data().text().len() {
                return core::new_text_range(0, 0);
            }
            return get_range_of_token_at_position(source_file, pos);
        }
        ast::Kind::FunctionDeclaration | ast::Kind::MethodDeclaration => {
            if !source_file
                .store()
                .flags(*node)
                .intersects(ast::NodeFlags::REPARSED)
            {
                ast::get_name_of_declaration(source_file.store(), Some(*node))
                    .map(ErrorNode::Owned)
                    .unwrap_or(ErrorNode::Missing)
            } else {
                ErrorNode::Borrowed(node)
            }
        }
        ast::Kind::VariableDeclaration
        | ast::Kind::BindingElement
        | ast::Kind::ClassDeclaration
        | ast::Kind::InterfaceDeclaration
        | ast::Kind::ModuleDeclaration
        | ast::Kind::EnumDeclaration
        | ast::Kind::EnumMember
        | ast::Kind::FunctionExpression
        | ast::Kind::GetAccessor
        | ast::Kind::SetAccessor
        | ast::Kind::TypeAliasDeclaration
        | ast::Kind::JSTypeAliasDeclaration
        | ast::Kind::PropertyDeclaration
        | ast::Kind::PropertySignature
        | ast::Kind::NamespaceImport => {
            ast::get_name_of_declaration(source_file.store(), Some(*node))
                .map(ErrorNode::Owned)
                .unwrap_or(ErrorNode::Missing)
        }
        ast::Kind::ClassExpression => source_file
            .store()
            .name(*node)
            .map(ErrorNode::Owned)
            .unwrap_or(ErrorNode::Missing),
        ast::Kind::ArrowFunction => {
            return get_error_range_for_arrow_function(source_file, node);
        }
        ast::Kind::CaseClause | ast::Kind::DefaultClause => {
            let start = skip_trivia(
                source_file.data().text(),
                node_pos(source_file, *node).max(0) as usize,
            );
            let mut end = node_end(source_file, *node);
            if let Some(first_statement) = source_file
                .store()
                .statements(*node)
                .and_then(|statements| statements.first())
            {
                end = node_pos(source_file, first_statement);
            }
            return core::new_text_range(start as i32, end);
        }
        ast::Kind::ReturnStatement | ast::Kind::YieldExpression => {
            let pos = skip_trivia(
                source_file.data().text(),
                node_pos(source_file, *node).max(0) as usize,
            );
            return get_range_of_token_at_position(source_file, pos);
        }
        ast::Kind::SatisfiesExpression => {
            if let Some(expression) = source_file.store().expression(*node) {
                let pos = skip_trivia(
                    source_file.data().text(),
                    node_end(source_file, expression).max(0) as usize,
                );
                return get_range_of_token_at_position(source_file, pos);
            }
            ErrorNode::Borrowed(node)
        }
        ast::Kind::Constructor => {
            if !source_file
                .store()
                .flags(*node)
                .intersects(ast::NodeFlags::REPARSED)
            {
                let mut scanner = get_scanner_for_source_file(
                    source_file,
                    node_pos(source_file, *node).max(0) as usize,
                );
                let start = scanner.token_start();
                while scanner.token() != ast::Kind::ConstructorKeyword
                    && scanner.token() != ast::Kind::StringLiteral
                    && scanner.token() != ast::Kind::EndOfFile
                {
                    scanner.scan();
                }
                return core::new_text_range(start, scanner.token_end());
            }
            ErrorNode::Borrowed(node)
        }
        _ => ErrorNode::Borrowed(node),
    };

    let Some(error_node) = error_node.as_ref() else {
        return get_range_of_token_at_position(
            source_file,
            node_pos(source_file, *node).max(0) as usize,
        );
    };

    let mut pos = node_pos(source_file, *error_node);
    if !node_is_missing(source_file, *error_node)
        && node_kind(source_file, *error_node) != ast::Kind::JsxText
    {
        pos = skip_trivia(source_file.data().text(), pos.max(0) as usize) as i32;
    }
    core::new_text_range(pos, node_end(source_file, *error_node))
}

pub trait TextPositionArg {
    fn to_usize(self) -> usize;
}

impl TextPositionArg for usize {
    fn to_usize(self) -> usize {
        self
    }
}

impl TextPositionArg for i32 {
    fn to_usize(self) -> usize {
        self.max(0) as usize
    }
}

pub fn compute_line_of_position(line_starts: &[core::TextPos], pos: impl TextPositionArg) -> usize {
    let pos = pos.to_usize() as core::TextPos;
    match line_starts.binary_search(&pos) {
        Ok(index) => index,
        Err(index) => index.saturating_sub(1),
    }
}

pub fn get_ecma_line_starts(source_file: impl ast::SourceFileLike) -> Arc<[core::TextPos]> {
    source_file.ecma_line_map()
}

pub fn get_ecma_line_of_position(
    source_file: impl ast::SourceFileLike,
    pos: impl TextPositionArg,
) -> usize {
    compute_line_of_position(&get_ecma_line_starts(source_file), pos)
}

pub fn get_ecma_line_and_utf16_character_of_position(
    source_file: impl ast::SourceFileLike,
    pos: impl TextPositionArg,
) -> (usize, core::UTF16Offset) {
    let pos = pos.to_usize();
    let text = source_file.text();
    let line_map = get_ecma_line_starts(source_file);
    let line = compute_line_of_position(&line_map, pos);
    let line_start = line_map.get(line).copied().unwrap_or_default().max(0) as usize;
    let character = text[line_start..pos.min(text.len())]
        .chars()
        .map(|ch| ch.len_utf16() as core::UTF16Offset)
        .sum();
    (line, character)
}

pub fn get_ecma_line_and_byte_offset_of_position(
    source_file: impl ast::SourceFileLike,
    pos: impl TextPositionArg,
) -> (usize, usize) {
    let pos = pos.to_usize();
    let line_map = get_ecma_line_starts(source_file);
    let line = compute_line_of_position(&line_map, pos);
    let line_start = line_map.get(line).copied().unwrap_or_default().max(0) as usize;
    (line, pos.saturating_sub(line_start))
}

pub fn get_ecma_end_line_position(source_file: impl ast::SourceFileLike, line: usize) -> usize {
    let text = source_file.text();
    let line_map = get_ecma_line_starts(source_file);
    if line + 1 == line_map.len() {
        return text.len().saturating_sub(1);
    }
    let start = line_map.get(line).copied().unwrap_or_default().max(0) as usize;
    let mut pos = line_map[line + 1].max(0) as usize;
    pos = pos.saturating_sub(1);
    while start <= pos {
        let Some(ch) = text[..=pos].chars().next_back() else {
            break;
        };
        if !matches!(ch, '\r' | '\n' | '\u{2028}' | '\u{2029}') {
            break;
        }
        if pos == 0 {
            break;
        }
        pos = pos.saturating_sub(ch.len_utf8());
    }
    pos
}

pub fn get_ecma_position_of_line_and_byte_offset(
    source_file: impl ast::SourceFileLike,
    line: usize,
    byte_offset: usize,
) -> usize {
    compute_position_of_line_and_byte_offset(&get_ecma_line_starts(source_file), line, byte_offset)
}

pub fn get_ecma_position_of_line_and_utf16_character(
    source_file: impl ast::SourceFileLike,
    line: usize,
    character: core::UTF16Offset,
) -> usize {
    let text = source_file.text();
    compute_position_of_line_and_utf16_character(
        &get_ecma_line_starts(source_file),
        line,
        character,
        &text,
        false,
    )
}

pub fn compute_position_of_line_and_byte_offset(
    line_starts: &[core::TextPos],
    line: usize,
    byte_offset: usize,
) -> usize {
    line_starts
        .get(line)
        .copied()
        .unwrap_or_default()
        .saturating_add(byte_offset as core::TextPos) as usize
}

pub fn compute_position_of_line_and_utf16_character(
    line_starts: &[core::TextPos],
    line: usize,
    character: core::UTF16Offset,
    text: &str,
    allow_edits: bool,
) -> usize {
    let Some(&line_start) = line_starts.get(line) else {
        if !allow_edits {
            panic!(
                "Bad line number. Line: {}, lineStarts.length: {}.",
                line,
                line_starts.len()
            );
        }
        return if line_starts.is_empty() {
            0
        } else if line == 0 {
            line_starts[0].max(0) as usize
        } else {
            line_starts[line_starts.len() - 1].max(0) as usize
        };
    };

    let line_start = line_start.max(0) as usize;
    if character > 0 {
        let line_end = line_starts
            .get(line + 1)
            .copied()
            .map(|pos| pos.max(0) as usize)
            .unwrap_or(text.len());
        let mut utf16 = 0;
        let mut pos = line_start;
        while pos < line_end {
            if utf16 >= character {
                break;
            }
            let Some(ch) = text[pos..].chars().next() else {
                break;
            };
            utf16 += ch.len_utf16() as core::UTF16Offset;
            pos += ch.len_utf8();
        }
        if !allow_edits && pos == line_end && utf16 < character {
            panic!(
                "Bad UTF-16 character offset. Line: {}, character: {}.",
                line, character
            );
        }
        if allow_edits && pos > text.len() {
            return text.len();
        }
        pos
    } else if allow_edits && line_start > text.len() {
        text.len()
    } else {
        if !allow_edits {
            assert!(line_start <= text.len());
        }
        line_start
    }
}

fn scan_line_comment(text: &str, pos: usize) -> usize {
    text[pos..]
        .find(['\r', '\n'])
        .map(|offset| pos + offset)
        .unwrap_or(text.len())
}

fn scan_block_comment(text: &str, pos: usize) -> usize {
    text[pos + 2..]
        .find("*/")
        .map(|offset| pos + 2 + offset + 2)
        .unwrap_or(text.len())
}

pub fn get_leading_comment_ranges(text: &str, pos: i32) -> Vec<ast::CommentRange> {
    iterate_comment_ranges(text, pos, false)
}

pub fn get_trailing_comment_ranges(text: &str, pos: i32) -> Vec<ast::CommentRange> {
    iterate_comment_ranges(text, pos, true)
}

fn iterate_comment_ranges(text: &str, pos: i32, trailing: bool) -> Vec<ast::CommentRange> {
    if pos < 0 {
        return Vec::new();
    }

    let bytes = text.as_bytes();
    let mut pos = pos.min(bytes.len() as i32) as usize;
    let mut pending: Option<ast::CommentRange> = None;
    let mut ranges = Vec::new();
    let mut collecting = trailing;

    if pos == 0 {
        collecting = true;
        if is_shebang_trivia(text, pos) {
            pos = scan_shebang_trivia(text, pos);
        }
    }

    while pos < bytes.len() {
        match bytes[pos] {
            b'\r' | b'\n' => {
                let is_cr = bytes[pos] == b'\r';
                pos += 1;
                if is_cr && bytes.get(pos) == Some(&b'\n') {
                    pos += 1;
                }
                if trailing {
                    break;
                }
                collecting = true;
                if let Some(comment) = &mut pending {
                    comment.has_trailing_new_line = true;
                }
            }
            b'\t' | b'\x0b' | b'\x0c' | b' ' => pos += 1,
            b'/' if matches!(bytes.get(pos + 1), Some(b'/' | b'*')) => {
                let kind = if bytes[pos + 1] == b'/' {
                    ast::Kind::SingleLineCommentTrivia
                } else {
                    ast::Kind::MultiLineCommentTrivia
                };
                let start = pos;
                let mut has_trailing_new_line = false;
                pos += 2;
                if kind == ast::Kind::SingleLineCommentTrivia {
                    while pos < bytes.len() {
                        if matches!(bytes[pos], b'\r' | b'\n') {
                            has_trailing_new_line = true;
                            break;
                        }
                        pos += 1;
                    }
                } else {
                    while pos < bytes.len() {
                        if bytes[pos] == b'*' && bytes.get(pos + 1) == Some(&b'/') {
                            pos += 2;
                            break;
                        }
                        pos += 1;
                    }
                }

                if collecting
                    && let Some(comment) = pending.replace(ast::CommentRange {
                        text_range: core::new_text_range(start as i32, pos as i32),
                        kind,
                        has_trailing_new_line,
                    })
                {
                    ranges.push(comment);
                }
            }
            byte if byte > 0x7f => {
                let Some(ch) = text[pos..].chars().next() else {
                    break;
                };
                if !ch.is_whitespace() {
                    break;
                }
                if let Some(comment) = &mut pending
                    && matches!(ch, '\u{2028}' | '\u{2029}')
                {
                    comment.has_trailing_new_line = true;
                }
                pos += ch.len_utf8();
            }
            _ => break,
        }
    }

    if let Some(comment) = pending {
        ranges.push(comment);
    }
    ranges
}

