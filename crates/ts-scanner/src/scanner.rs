    pub fn rescan_slash_token(&mut self, report_errors: bool) -> ast::Kind {
        if self.token == ast::Kind::SlashToken || self.token == ast::Kind::SlashEqualsToken {
            let start_of_reg_exp_body = self.token_start + 1;
            let mut p = start_of_reg_exp_body;
            let mut in_escape = false;
            let mut named_capture_groups = false;
            let mut in_character_class = false;
            let text = self.text.as_bytes();

            loop {
                if p >= text.len() {
                    self.token_flags |= ast::TokenFlags::UNTERMINATED;
                    break;
                }
                let ch = text[p] as char;
                if is_line_break(ch) {
                    self.token_flags |= ast::TokenFlags::UNTERMINATED;
                    break;
                }
                if in_escape {
                    in_escape = false;
                } else if ch == '/' && !in_character_class {
                    break;
                } else if ch == '[' {
                    in_character_class = true;
                } else if ch == '\\' {
                    in_escape = true;
                } else if ch == ']' {
                    in_character_class = false;
                } else if !in_character_class
                    && ch == '('
                    && p + 1 < text.len()
                    && text[p + 1] == b'?'
                    && p + 2 < text.len()
                    && text[p + 2] == b'<'
                    && (p + 3 >= text.len() || (text[p + 3] != b'=' && text[p + 3] != b'!'))
                {
                    named_capture_groups = true;
                }
                p += 1;
            }

            let end_of_reg_exp_body = p;
            if self.token_flags.contains(ast::TokenFlags::UNTERMINATED) {
                p = start_of_reg_exp_body;
                in_escape = false;
                let mut character_class_depth = 0;
                let mut in_decimal_quantifier = false;
                let mut group_depth = 0;
                while p < end_of_reg_exp_body {
                    let ch = self.text.as_bytes()[p] as char;
                    if in_escape {
                        in_escape = false;
                    } else if ch == '\\' {
                        in_escape = true;
                    } else if ch == '[' {
                        character_class_depth += 1;
                    } else if ch == ']' && character_class_depth != 0 {
                        character_class_depth -= 1;
                    } else if character_class_depth == 0 {
                        if ch == '{' {
                            in_decimal_quantifier = true;
                        } else if ch == '}' && in_decimal_quantifier {
                            in_decimal_quantifier = false;
                        } else if !in_decimal_quantifier {
                            if ch == '(' {
                                group_depth += 1;
                            } else if ch == ')' && group_depth != 0 {
                                group_depth -= 1;
                            } else if ch == ')' || ch == ']' || ch == '}' {
                                break;
                            }
                        }
                    }
                    p += 1;
                }
                while p > start_of_reg_exp_body {
                    let Some(ch) = self.text[..p].chars().next_back() else {
                        break;
                    };
                    if is_white_space_like(ch) || ch == ';' {
                        p -= ch.len_utf8();
                    } else {
                        break;
                    }
                }
                self.error_at(
                    &diagnostics::Unterminated_regular_expression_literal,
                    self.token_start,
                    p.saturating_sub(self.token_start),
                    vec![],
                );
            } else {
                p += 1;
                let mut reg_exp_flags = RegularExpressionFlags::None as i32;
                while p < self.end {
                    let Some(ch) = self.text[p..self.end].chars().next() else {
                        break;
                    };
                    let size = ch.len_utf8();
                    if !is_identifier_part(ch) {
                        break;
                    }
                    if report_errors {
                        if let Some(flag) = char_code_to_reg_exp_flag(ch) {
                            let flag_value = flag as i32;
                            if reg_exp_flags & flag_value != 0 {
                                self.error_at(
                                    &diagnostics::Duplicate_regular_expression_flag,
                                    p,
                                    size,
                                    vec![],
                                );
                            } else if (reg_exp_flags | flag_value)
                                & REGULAR_EXPRESSION_FLAGS_ANY_UNICODE_MODE
                                == REGULAR_EXPRESSION_FLAGS_ANY_UNICODE_MODE
                            {
                                self.error_at(
                                    &diagnostics::The_Unicode_u_flag_and_the_Unicode_Sets_v_flag_cannot_be_set_simultaneously,
                                    p,
                                    size,
                                    vec![],
                                );
                            } else {
                                reg_exp_flags |= flag_value;
                                self.check_regular_expression_flag_availability(flag, p, size);
                            }
                        } else {
                            self.error_at(
                                &diagnostics::Unknown_regular_expression_flag,
                                p,
                                size,
                                vec![],
                            );
                        }
                    }
                    p += size;
                }

                if report_errors {
                    self.pos = start_of_reg_exp_body;
                    let save_token_pos = self.token_start;
                    let save_token_flags = self.token_flags;
                    {
                        let mut parser = RegExpParser {
                            scanner: self,
                            end: end_of_reg_exp_body,
                            reg_exp_flags,
                            any_unicode_mode: reg_exp_flags
                                & REGULAR_EXPRESSION_FLAGS_ANY_UNICODE_MODE
                                != 0,
                            unicode_sets_mode: reg_exp_flags
                                & RegularExpressionFlags::UnicodeSets as i32
                                != 0,
                            annex_b: true,
                            any_unicode_mode_or_non_annex_b: false,
                            named_capture_groups,
                            may_contain_strings: false,
                            number_of_capturing_groups: 0,
                            group_specifiers: HashMap::new(),
                            group_name_references: Vec::new(),
                            decimal_escapes: Vec::new(),
                            named_capturing_groups: Vec::new(),
                            pending_low_surrogate: None,
                        };
                        parser.run();
                    }
                    self.pos = p;
                    self.token_start = save_token_pos;
                    self.token_flags = save_token_flags;
                } else {
                    self.pos = p;
                }
            }

            self.pos = p;
            self.set_token_value_from_range(self.token_start, self.pos);
            self.token = ast::Kind::RegularExpressionLiteral;
        }
        self.token
    }

