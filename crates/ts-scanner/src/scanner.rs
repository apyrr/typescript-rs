    pub fn scan_jsx_token_ex(&mut self, allow_multiline_jsx_text: bool) -> ast::Kind {
        self.full_start_pos = self.pos;
        self.token_start = self.pos;
        match self.char() {
            '\0' => {
                self.token = ast::Kind::EndOfFile;
            }
            '<' => {
                if self.char_at(1) == '/' {
                    self.pos += 2;
                    self.token = ast::Kind::LessThanSlashToken;
                } else {
                    self.pos += 1;
                    self.token = ast::Kind::LessThanToken;
                }
            }
            '{' => {
                self.pos += 1;
                self.token = ast::Kind::OpenBraceToken;
            }
            _ => {
                let mut first_non_whitespace = 0_isize;
                loop {
                    let (ch, size) = self.char_and_size();
                    if size == 0 || ch == '{' {
                        break;
                    }
                    if ch == '<' {
                        if is_conflict_marker_trivia(&self.text, self.pos) {
                            self.error_at(
                                &diagnostics::Merge_conflict_marker_encountered,
                                self.pos,
                                7,
                                vec![],
                            );
                            self.pos = scan_conflict_marker_trivia(&self.text, self.pos);
                            self.token = ast::Kind::ConflictMarkerTrivia;
                            return self.token;
                        }
                        break;
                    }
                    if ch == '>' {
                        self.error_at(
                            &diagnostics::Unexpected_token_Did_you_mean_or_gt,
                            self.pos,
                            1,
                            vec![],
                        );
                    } else if ch == '}' {
                        self.error_at(
                            &diagnostics::Unexpected_token_Did_you_mean_or_rbrace,
                            self.pos,
                            1,
                            vec![],
                        );
                    }
                    if is_line_break(ch) && first_non_whitespace == 0 {
                        first_non_whitespace = -1;
                    } else if !allow_multiline_jsx_text
                        && is_line_break(ch)
                        && first_non_whitespace > 0
                    {
                        break;
                    } else if !is_white_space_like(ch) {
                        first_non_whitespace = self.pos as isize;
                    }
                    self.pos += size;
                }
                self.set_token_value_from_range(self.full_start_pos, self.pos);
                self.token = if first_non_whitespace == -1 {
                    ast::Kind::JsxTextAllWhiteSpaces
                } else {
                    ast::Kind::JsxText
                };
            }
        }
        self.token
    }

    pub fn scan_jsx_identifier(&mut self) -> ast::Kind {
        if token_is_identifier_or_keyword(self.token) {
            loop {
                let ch = self.char();
                if ch == '\0' {
                    break;
                }
                if ch == '-' {
                    self.push_token_value('-');
                    self.pos += 1;
                    continue;
                }
                let old_pos = self.pos;
                let parts = self.scan_identifier_parts();
                self.push_str_token_value(&parts);
                if self.pos == old_pos {
                    break;
                }
            }
            self.token = get_identifier_token(self.token_value());
        }
        self.token
    }

    pub fn scan_jsx_attribute_value(&mut self) -> ast::Kind {
        self.full_start_pos = self.pos;
        loop {
            let (ch, size) = self.char_and_size();
            if size == 0 || !is_white_space_like(ch) {
                break;
            }
            self.pos += size;
        }
        self.token_start = self.pos;
        match self.char() {
            '"' | '\'' => {
                self.scan_string_and_set_token_value(true);
                self.token = ast::Kind::StringLiteral;
                self.token
            }
            _ => self.scan(),
        }
    }

