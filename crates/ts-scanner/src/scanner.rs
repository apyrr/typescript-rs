pub fn is_identifier_start(ch: char) -> bool {
    ch.is_ascii_alphabetic()
        || ch == '_'
        || ch == '$'
        || !ch.is_ascii() && is_unicode_identifier_start(ch)
}

pub fn is_identifier_part(ch: char) -> bool {
    ch.is_ascii_alphanumeric()
        || ch == '_'
        || ch == '$'
        || !ch.is_ascii() && is_unicode_identifier_part(ch)
}

pub fn is_identifier_part_ex(ch: char, language_variant: core::LanguageVariant) -> bool {
    is_identifier_part(ch)
        || (language_variant == core::LanguageVariant::JSX && (ch == '-' || ch == ':'))
}

pub fn is_unicode_identifier_start(ch: char) -> bool {
    is_in_unicode_ranges(ch, UNICODE_ES_NEXT_IDENTIFIER_START)
}

pub fn is_unicode_identifier_part(ch: char) -> bool {
    is_in_unicode_ranges(ch, UNICODE_ES_NEXT_IDENTIFIER_PART)
}

pub fn is_in_unicode_ranges(cp: char, ranges: &[u32]) -> bool {
    let cp = cp as u32;
    // Bail out quickly if it couldn't possibly be in the map
    if cp < ranges[0] {
        return false;
    }
    // Perform binary search in one of the Unicode range maps
    let mut lo = 0;
    let mut hi = ranges.len();
    while lo + 1 < hi {
        let mut mid = lo + (hi - lo) / 2;
        // mid has to be even to catch beginning of a range
        mid -= mid % 2;
        if ranges[mid] <= cp && cp <= ranges[mid + 1] {
            return true;
        }
        if cp < ranges[mid] {
            hi = mid;
        } else {
            lo = mid + 2;
        }
    }
    false
}

