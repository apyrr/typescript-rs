function isConcreteMarkerBaseField(extKey: string): boolean {
    return concreteMarkerBaseFields.has(extKey);
}

function structFieldsFor(node: NodeType): StructField[] {
    const fields: StructField[] = [];
    const names = new Set<string>();
    for (const extKey of node.extendsKeys) {
        if (extKey === "NodeBase") continue;
        const ext = api.getBase(extKey);
        if (!ext) continue;
        for (const inherited of collectInheritedStructFields(ext)) {
            if (inherited.type.endsWith("Base")) continue;
            pushUniqueField(fields, names, inherited);
        }
        for (const inherited of ext.fields
            .filter((field: MemberInfo) => !field.noGo && !isSemanticPayloadMember(field))
            .map(memberToStructField)) {
            pushUniqueField(fields, names, inherited);
        }
    }
    for (const member of node.members) {
        if (member.noGo) continue;
        if (isSemanticPayloadMember(member)) continue;
        if (member.isKindParam()) continue;
        if (isNodeFlagsMember(member)) continue;
        if (member.inherited) continue;
        pushUniqueField(fields, names, memberToStructField(member));
    }
    return fields;
}

function generateKind(): string {
    const w = new CodeWriter();
    for (const line of generatedHeader(generatedSource("vendor/typescript-go/_scripts/ast.json"))) w.write(line);
    w.write("#![expect(non_upper_case_globals, reason = \"generated SyntaxKind marker names mirror TypeScript-Go\")]");
    w.write("");
    w.write("#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Default)]");
    w.write("#[repr(i16)]");
    w.write("pub enum Kind {");
    w.push();
    let first = true;
    for (const element of api.kindElements()) {
        if (!element.name) {
            w.write(`// ${element.comment ?? ""}`);
            continue;
        }
        const suffix = element.comment ? `, // ${element.comment}` : ",";
        if (first) {
            w.write("#[default]");
            w.write(`${element.name} = 0${suffix}`);
            first = false;
        }
        else {
            w.write(`${element.name}${suffix}`);
        }
    }
    w.write("Count,");
    w.pop();
    w.write("}");
    w.write("");
    w.write("pub const KIND_COUNT: i16 = Kind::Count as i16;");
    w.write("");
    w.write("impl Kind {");
    w.push();
    w.write("pub fn next(self) -> Self {");
    w.push();
    w.write("match self {");
    w.push();
    const kindElements = api.kindElements().filter((element: any) => element.name);
    for (let i = 0; i < kindElements.length; i++) {
        const current = kindElements[i].name;
        const next = kindElements[i + 1]?.name ?? "Count";
        w.write(`Kind::${current} => Kind::${next},`);
    }
    w.write("Kind::Count => Kind::Count,");
    w.pop();
    w.write("}");
    w.pop();
    w.write("}");
    w.write("");
    for (const marker of api.kindMarkers()) {
        w.write(`pub const ${marker.name}: Kind = ${rustKindConstant(marker.value)};`);
    }
    w.pop();
    w.write("}");
    w.write("");
    for (const { name, members } of api.kindAliases()) {
        const expanded = expandKindMembers(members);
        w.write(`pub type ${name} = Kind; // ${expanded.map(member => `Kind::${member}`).join(" | ")}`);
    }
    return w.toString();
}

function expandKindMembers(members: string[]): string[] {
    const result: string[] = [];
    for (const member of members) {
        if (isJSDocKindReference(member)) continue;
        const alias = api.getKindAlias(member);
        if (alias) result.push(...expandKindMembers(alias.members));
        else result.push(member);
    }
    return result;
}

function generateKindAliases(): string {
    const w = new CodeWriter();
    for (const line of generatedHeader(generatedSource("vendor/typescript-go/_scripts/ast.json"))) w.write(line);
    w.write("use crate::Kind;");
    w.write("");
    for (const element of api.kindElements()) {
        if (!element.name) continue;
        w.write(`pub const KIND_${screamingSnakeCase(element.name)}: Kind = Kind::${element.name};`);
    }
    return w.toString();
}

