#!/usr/bin/env bun

// Generates Rust AST compatibility files from vendor/typescript-go/_scripts/ast.json.
// Keep this schema-driven; do not depend on a local copy of schema.ts.

import * as fs from "node:fs";
import * as path from "node:path";
import { pathToFileURL } from "node:url";

type MemberInfo = any;
type NodeType = any;
type Type = any;

const repoRoot = path.resolve(import.meta.dirname, "..");
const defaultOutputDir = path.join(repoRoot, "crates", "ts-ast", "src");
const handwrittenAstPath = path.join(defaultOutputDir, "ast.rs");
let handwrittenAst: string | undefined;
let goVisitRoutes: Map<string, string[]> | undefined;
let api: any;
let kindGuardName: (aliasName: string) => string;

async function loadSchemaApi(): Promise<void> {
    const schemaModuleUrl = pathToFileURL(path.join(repoRoot, "vendor", "typescript-go", "_scripts", "schema.ts")).href;
    const schemaModule = await import(schemaModuleUrl);
    api = withoutJSDoc(schemaModule.api);
    kindGuardName = schemaModule.kindGuardName;
}

function isJSDocSchemaName(name: unknown): boolean {
    return typeof name === "string" && name.includes("JSDoc");
}

function isJSDocKindReference(value: unknown): boolean {
    if (typeof value !== "string") return false;
    const name = value.startsWith("SyntaxKind.") ? value.slice("SyntaxKind.".length) : value;
    return isJSDocSchemaName(name);
}

function withoutJSDoc(upstreamApi: any): any {
    const filteredAliases = new Map<string, any>();
    for (const alias of upstreamApi.kindAliases()) {
        if (isJSDocSchemaName(alias.name)) continue;
        const members = alias.members.filter((member: string) => !isJSDocKindReference(member));
        if (members.length === 0) continue;
        filteredAliases.set(alias.name, { ...alias, members });
    }

    const filteredApi = Object.create(upstreamApi);
    filteredApi.nodes = () => upstreamApi.nodes().filter((node: NodeType) => !isJSDocSchemaName(node.name));
    filteredApi.bases = () => upstreamApi.bases().filter((base: NodeType) => !isJSDocSchemaName(base.key));
    filteredApi.getBase = (key: string) => isJSDocSchemaName(key) ? undefined : upstreamApi.getBase(key);
    filteredApi.kindElements = () => upstreamApi.kindElements().filter((element: any) =>
        !isJSDocSchemaName(element.name) && !isJSDocSchemaName(element.comment)
    );
    filteredApi.kindMarkers = () => upstreamApi.kindMarkers().filter((marker: any) =>
            !isJSDocSchemaName(marker.name) && !isJSDocKindReference(marker.value)
        );
    filteredApi.kindAliases = () => [...filteredAliases.values()];
    filteredApi.getKindAlias = (name: string) => filteredAliases.get(name);
    filteredApi.kindGuards = () => upstreamApi.kindGuards()
        .filter((guard: any) =>
            !isJSDocSchemaName(guard.aliasName) &&
            !isJSDocKindReference(guard.first) &&
            !isJSDocKindReference(guard.last)
        )
        .map((guard: any) => guard.members
            ? { ...guard, members: guard.members.filter((member: string) => !isJSDocKindReference(member)) }
            : guard
        )
        .filter((guard: any) => guard.type !== "list" || guard.members.length > 0);
    return filteredApi;
}

class CodeWriter {
    private readonly lines: string[] = [];
    private indent = 0;

    write(line = ""): void {
        this.lines.push(line === "" ? "" : `${"    ".repeat(this.indent)}${line}`);
    }

    push(): void {
        this.indent++;
    }

    pop(): void {
        this.indent--;
    }

    toString(): string {
        return this.lines.join("\n");
    }
}

function generatedHeader(source: string): string[] {
    return [
        "// Code generated by scripts/generate-go-ast.ts.",
        `// Source: ${source}`,
        "",
    ];
}

function snakeCase(name: string): string {
    return name
        .replace(/([A-Z]+)([A-Z][a-z])/g, "$1_$2")
        .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
        .replace(/J_S_Doc/g, "JSDoc")
        .replace(/JS_Doc/g, "JSDoc")
        .toLowerCase();
}

function screamingSnakeCase(name: string): string {
    return snakeCase(name).toUpperCase();
}

function fieldName(name: string): string {
    const result = snakeCase(name);
    return result === "type" ? "r#type" : result;
}

function paramName(member: MemberInfo): string {
    const name = fieldName(member.name);
    if (name === "default") return "default_node";
    if (name === "case") return "case_node";
    return name === "r#type" ? "type_node" : name;
}

function schemaMembers(node: NodeType): MemberInfo[] {
    return node.members.filter((member: MemberInfo) => !member.noFactory);
}

function isNodeFlagsMember(member: MemberInfo): boolean {
    return member.type.kind === "primitive" && member.type.name === "NodeFlags";
}

function isOptionalChainFactory(node: NodeType): boolean {
    return new Set([
        "PropertyAccessExpression",
        "ElementAccessExpression",
        "CallExpression",
        "NonNullExpression",
        "TaggedTemplateExpression",
    ]).has(node.name);
}

function isTextContentType(type: Type): boolean {
    if (type.kind === "primitive") return type.name === "string";
    return type.kind === "list" &&
        type.listKind === "raw" &&
        type.elementType.kind === "primitive" &&
        type.elementType.name === "string";
}

function isStringType(type: Type): boolean {
    return type.kind === "primitive" && type.name === "string";
}

function hasTextContent(node: NodeType): boolean {
    return schemaMembers(node).some(member => isTextContentType(member.type));
}

function hasHandwrittenMethod(typeName: string, methodName: string): boolean {
    handwrittenAst ??= fs.existsSync(handwrittenAstPath) ? fs.readFileSync(handwrittenAstPath, "utf-8") : "";
    const lines = handwrittenAst.split(/\r?\n/);
    let inImpl = false;
    let depth = 0;
    for (const line of lines) {
        if (!inImpl && new RegExp(`^\\s*impl\\s+${typeName}\\s*\\{\\s*$`).test(line)) {
            inImpl = true;
            depth = 0;
        }
        if (!inImpl) continue;
        if (new RegExp(`\\bfn\\s+${methodName}\\s*\\(`).test(line)) return true;
        depth += (line.match(/\{/g) ?? []).length;
        depth -= (line.match(/\}/g) ?? []).length;
        if (depth <= 0) inImpl = false;
    }
    return false;
}

function rustKindConstant(kindName: string): string {
    return `Kind::${kindName.startsWith("SyntaxKind.") ? kindName.slice("SyntaxKind.".length) : kindName}`;
}

function typeName(type: Type, optional = false): string {
    let result = "Node";
    switch (type.kind) {
        case "primitive":
            result = primitiveTypeName(type.name);
            break;
        case "kind":
            result = optional ? "Option<Kind>" : "Kind";
            break;
        case "node":
            result = "OptionalAstNodeId";
            break;
        case "alias":
            result = type.resolved.baseKind() === "node" ? "OptionalAstNodeId" : typeName(type.resolved, optional);
            break;
        case "typeParameter":
            result = typeName(type.constraint, optional);
            break;
        case "union":
            result = type.types.every((member: Type) => member.baseKind() === "node")
                ? "OptionalAstNodeId"
                : type.types.every((member: Type) => member.baseKind() === "kind")
                    ? (optional ? "Option<Kind>" : "Kind")
                    : "OptionalAstNodeId";
            break;
        case "list":
            if (type.listKind === "ModifierList") {
                result = optional ? "OptionalModifierListId" : "ModifierListId";
            }
            else if (type.listKind === "NodeList") {
                result = optional ? "OptionalNodeListId" : "NodeListId";
            }
            else if (type.elementType.baseKind() === "node") {
                result = optional ? "OptionalRawNodeSliceId" : "RawNodeSliceId";
            }
            else if (type.elementType.kind === "primitive" && type.elementType.name === "string") {
                result = optional ? "OptionalRawStringSliceId" : "RawStringSliceId";
            }
            else {
                result = `Vec<${typeName(type.elementType)}>`;
            }
            break;
    }
    return result;
}

function paramBaseTypeName(type: Type): string {
    switch (type.kind) {
        case "primitive":
            return primitiveTypeName(type.name);
        case "kind":
            return "Kind";
        case "node":
            return api.hasNode(type.name) ? "Node" : type.name;
        case "alias":
            return type.resolved.baseKind() === "node" ? "Node" : paramBaseTypeName(type.resolved);
        case "typeParameter":
            return paramBaseTypeName(type.constraint);
        case "union":
            if (type.types.every((member: Type) => member.baseKind() === "kind")) return "Kind";
            return "Node";
        case "list":
            if (type.listKind === "ModifierList") return "ModifierList";
            if (type.listKind === "NodeList") return "NodeList";
            if (type.elementType.baseKind() === "node") return "RawNodeSlice";
            if (type.elementType.kind === "primitive" && type.elementType.name === "string") return "RawStringSlice";
            return `Vec<${paramBaseTypeName(type.elementType)}>`;
    }
    return "Node";
}

function aggregateParamTraitName(type: Type, optional: boolean): string | undefined {
    if (type.kind !== "list") return undefined;
    if (type.listKind === "ModifierList") return optional ? "IntoOptionalModifierList" : "IntoModifierList";
    if (type.listKind === "NodeList") return optional ? "IntoOptionalNodeList" : "IntoNodeList";
    if (type.elementType.baseKind() === "node") return optional ? "IntoOptionalRawNodeSlice" : "IntoRawNodeSlice";
    if (type.elementType.kind === "primitive" && type.elementType.name === "string") return optional ? "IntoOptionalRawStringSlice" : "IntoRawStringSlice";
    return undefined;
}

function goRawTypeName(rawType: string | string[]): string | undefined {
    if (Array.isArray(rawType)) return undefined;
    if (rawType === "*Symbol") {
        return "Option<SymbolRef>";
    }
    if (rawType === "*FlowNode" || rawType === "*FlowLabel") {
        return "Option<FlowRef>";
    }
    if (rawType === "*FlowList") {
        return "Option<FlowListRef>";
    }
    if (rawType === "*Node") {
        return "OptionalAstNodeId";
    }
    if (rawType.startsWith("*")) {
        return `Option<std::ptr::NonNull<${rawType.slice(1)}>>`;
    }
    if (rawType.startsWith("[]*")) {
        return "RawNodeSliceId";
    }
    if (rawType.startsWith("[]")) {
        return `Vec<${goRawTypeName(rawType.slice(2)) ?? rawType.slice(2)}>`;
    }
    switch (rawType) {
        case "atomic.Uint32":
            return "AtomicU32";
        case "atomic.Int32":
            return "AtomicI32";
        case "string":
            return "String";
        case "bool":
            return "bool";
        case "int":
            return "i32";
        default:
            return undefined;
    }
}

function primitiveTypeName(name: string): string {
    switch (name) {
        case "bool":
        case "boolean":
            return "bool";
        case "int":
            return "i32";
        case "string":
            return "String";
        case "any":
            return "()";
        default:
            return name;
    }
}

function paramType(member: MemberInfo): string {
    const base = paramBaseTypeName(member.type);
    if (isStringType(member.type)) return "impl Into<String>";
    if (isForeignNodeHandleMember(member)) return "impl Into<Option<Node>>";
    const aggregateTrait = aggregateParamTraitName(member.type, member.optional);
    if (aggregateTrait) return `impl ${aggregateTrait}`;
    if (member.type.baseKind() === "node") return `impl Into<Option<${base}>>`;
    if (member.optional) return `impl Into<Option<${base}>>`;
    return base;
}

function storedAsOption(member: MemberInfo): boolean {
    return member.optional ||
        member.type.baseKind() === "node" ||
        (member.type.kind === "list" && member.type.listKind !== "raw");
}

function fieldType(member: MemberInfo): string {
    if (isForeignNodeHandleMember(member)) return "Option<Node>";
    if (member.goOnly) {
        const raw = goRawTypeName(member.rawType);
        if (raw) return raw;
    }
    return typeName(member.type, member.optional);
}

function isForeignNodeHandleMember(member: MemberInfo): boolean {
    return member.name === "TupleNameSource";
}

function baseFieldName(baseName: string): string {
    return fieldName(baseName);
}

type StructField = { name: string; type: string; comment?: string };

const semanticPayloadFieldNames = new Set([
    "Symbol",
    "LocalSymbol",
    "Locals",
    "NextContainer",
    "FlowNode",
    "EndFlowNode",
    "ReturnFlowNode",
    "FallthroughFlowNode",
]);

function isSemanticPayloadMember(member: MemberInfo): boolean {
    return semanticPayloadFieldNames.has(member.name);
}

function collectInheritedStructFields(base: NodeType, seen = new Set<string>()): StructField[] {
    const result: StructField[] = [];
    for (const extKey of base.extendsKeys) {
        if (extKey === "NodeBase") continue;
        if (seen.has(extKey)) continue;
        seen.add(extKey);
        result.push({ name: baseFieldName(extKey), type: extKey });
        const ext = api.getBase(extKey);
        if (ext) {
            result.push(...collectInheritedStructFields(ext, seen));
            result.push(...ext.fields
                .filter((field: MemberInfo) => !field.noGo && !isSemanticPayloadMember(field))
                .map(memberToStructField));
        }
    }
    return result;
}

function memberToStructField(member: MemberInfo): StructField {
    return {
        name: fieldName(member.name),
        type: fieldType(member),
        comment: member.optional ? "Optional" : undefined,
    };
}

function pushUniqueField(fields: StructField[], names: Set<string>, field: StructField): void {
    if (names.has(field.name)) return;
    names.add(field.name);
    fields.push(field);
}

const concreteMarkerBaseFields = new Set([
    "AccessorDeclarationBase",
    "ExpressionBase",
    "FunctionLikeWithBodyBase",
    "FunctionOrConstructorTypeNodeBase",
    "IterationStatementBase",
    "UnaryExpressionBase",
    "UpdateExpressionBase",
    "LeftHandSideExpressionBase",
    "MemberExpressionBase",
    "NodeWithTypeArgumentsBase",
    "ObjectLiteralElementBase",
    "PrimaryExpressionBase",
    "LiteralExpressionBase",
    "StatementBase",
    "TypeElementBase",
    "ClassElementBase",
    "TypeNodeBase",
    "TypeSyntaxBase",
    "UnionOrIntersectionTypeNodeBase",
]);

function isConcreteMarkerBaseField(extKey: string): boolean {
    return concreteMarkerBaseFields.has(extKey);
}

function structFieldsFor(node: NodeType): StructField[] {
    const fields: StructField[] = [];
    const names = new Set<string>();
    for (const extKey of node.extendsKeys) {
        if (extKey === "NodeBase") continue;
        const ext = api.getBase(extKey);
        if (!ext) continue;
        for (const inherited of collectInheritedStructFields(ext)) {
            if (inherited.type.endsWith("Base")) continue;
            pushUniqueField(fields, names, inherited);
        }
        for (const inherited of ext.fields
            .filter((field: MemberInfo) => !field.noGo && !isSemanticPayloadMember(field))
            .map(memberToStructField)) {
            pushUniqueField(fields, names, inherited);
        }
    }
    for (const member of node.members) {
        if (member.noGo) continue;
        if (isSemanticPayloadMember(member)) continue;
        if (member.isKindParam()) continue;
        if (isNodeFlagsMember(member)) continue;
        if (member.inherited) continue;
        pushUniqueField(fields, names, memberToStructField(member));
    }
    return fields;
}

function generateKind(): string {
    const w = new CodeWriter();
    for (const line of generatedHeader("vendor/typescript-go/_scripts/ast.json")) w.write(line);
    w.write("#![expect(non_upper_case_globals, reason = \"generated SyntaxKind marker names mirror TypeScript-Go\")]");
    w.write("");
    w.write("#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Default)]");
    w.write("#[repr(i16)]");
    w.write("pub enum Kind {");
    w.push();
    let first = true;
    for (const element of api.kindElements()) {
        if (!element.name) {
            w.write(`// ${element.comment ?? ""}`);
            continue;
        }
        const suffix = element.comment ? `, // ${element.comment}` : ",";
        if (first) {
            w.write("#[default]");
            w.write(`${element.name} = 0${suffix}`);
            first = false;
        }
        else {
            w.write(`${element.name}${suffix}`);
        }
    }
    w.write("Count,");
    w.pop();
    w.write("}");
    w.write("");
    w.write("pub const KIND_COUNT: i16 = Kind::Count as i16;");
    w.write("");
    w.write("impl Kind {");
    w.push();
    w.write("pub fn next(self) -> Self {");
    w.push();
    w.write("match self {");
    w.push();
    const kindElements = api.kindElements().filter((element: any) => element.name);
    for (let i = 0; i < kindElements.length; i++) {
        const current = kindElements[i].name;
        const next = kindElements[i + 1]?.name ?? "Count";
        w.write(`Kind::${current} => Kind::${next},`);
    }
    w.write("Kind::Count => Kind::Count,");
    w.pop();
    w.write("}");
    w.pop();
    w.write("}");
    w.write("");
    for (const marker of api.kindMarkers()) {
        w.write(`pub const ${marker.name}: Kind = ${rustKindConstant(marker.value)};`);
    }
    w.pop();
    w.write("}");
    w.write("");
    for (const { name, members } of api.kindAliases()) {
        const expanded = expandKindMembers(members);
        w.write(`pub type ${name} = Kind; // ${expanded.map(member => `Kind::${member}`).join(" | ")}`);
    }
    return w.toString();
}

function expandKindMembers(members: string[]): string[] {
    const result: string[] = [];
    for (const member of members) {
        if (isJSDocKindReference(member)) continue;
        const alias = api.getKindAlias(member);
        if (alias) result.push(...expandKindMembers(alias.members));
        else result.push(member);
    }
    return result;
}

function generateKindAliases(): string {
    const w = new CodeWriter();
    for (const line of generatedHeader("vendor/typescript-go/_scripts/ast.json")) w.write(line);
    w.write("use crate::Kind;");
    w.write("");
    for (const element of api.kindElements()) {
        if (!element.name) continue;
        w.write(`pub const KIND_${screamingSnakeCase(element.name)}: Kind = Kind::${element.name};`);
    }
    return w.toString();
}

