function generateStructAccessors(w: CodeWriter): void {
    const entries: { name: string; fields: StructField[]; isNode: boolean }[] = [];
    for (const base of api.bases()) {
        if (base.key === "NodeBase") continue;
        entries.push({ name: base.key, fields: structFieldsForBase(base), isNode: false });
    }
    for (const node of api.nodes()) {
        if (node.handWritten) continue;
        entries.push({ name: node.name, fields: structFieldsFor(node), isNode: true });
    }
    const handwrittenNameAccessors = new Set(["NamespaceImport", "ImportSpecifier"]);
    for (const entry of entries) {
        const nameField = entry.isNode && !handwrittenNameAccessors.has(entry.name)
            ? entry.fields.find(field => field.name === "name")
            : undefined;
        if (!nameField && entry.name !== "ImportClause") continue;
        w.write(`impl ${entry.name} {`);
        w.push();
        void nameField;
        if (entry.name === "ImportClause") {
            w.write("pub fn is_type_only(&self) -> bool {");
            w.push();
            w.write("self.phase_modifier == Some(Kind::TypeKeyword)");
            w.pop();
            w.write("}");
        }
        w.pop();
        w.write("}");
        w.write("");
    }
}

