function baseExtendsBase(base: NodeType, targetBaseName: string): boolean {
    for (const extKey of base.extendsKeys) {
        if (extKey === targetBaseName) return true;
        const ext = api.getBase(extKey);
        if (ext && baseExtendsBase(ext, targetBaseName)) return true;
    }
    return false;
}

function nodeExtendsBase(node: NodeType, targetBaseName: string): boolean {
    for (const extKey of node.extendsKeys) {
        if (extKey === targetBaseName) return true;
        const ext = api.getBase(extKey);
        if (ext && baseExtendsBase(ext, targetBaseName)) return true;
    }
    return false;
}

function cloneFieldExpr(source: string, field: StructField): string {
    if (field.type === "AtomicU32") {
        return `AtomicU32::new(${source}.${field.name}.load(Ordering::Relaxed))`;
    }
    if (field.type === "AtomicI32") {
        return `AtomicI32::new(${source}.${field.name}.load(Ordering::Relaxed))`;
    }
    if (
        field.type === "bool" ||
        field.type === "i32" ||
        field.type === "Kind" ||
        field.type === "NodeFlags" ||
        field.type === "ModifierFlags" ||
        field.type === "AstNodeId" ||
        field.type === "OptionalAstNodeId" ||
        field.type === "NodeListId" ||
        field.type === "OptionalNodeListId" ||
        field.type === "ModifierListId" ||
        field.type === "OptionalModifierListId" ||
        field.type === "RawNodeSliceId" ||
        field.type === "OptionalRawNodeSliceId" ||
        field.type === "RawStringSliceId" ||
        field.type === "OptionalRawStringSliceId"
    ) {
        return `${source}.${field.name}`;
    }
    return `${source}.${field.name}.clone()`;
}

function baseStructExpr(baseName: string, source: string): string {
    const base = api.getBase(baseName);
    if (!base) throw new Error(`Unknown base ${baseName}`);
    const fields = structFieldsForBase(base).map(field => {
        const value = field.type.endsWith("Base")
            ? baseStructExpr(field.type, source)
            : cloneFieldExpr(source, field);
        return `${field.name}: ${value}`;
    });
    return `${baseName} { ${fields.join(", ")} }`;
}

function baseStructUsesSource(baseName: string): boolean {
    const base = api.getBase(baseName);
    if (!base) throw new Error(`Unknown base ${baseName}`);
    return structFieldsForBase(base).some(field =>
        field.type.endsWith("Base") ? baseStructUsesSource(field.type) : true
    );
}

